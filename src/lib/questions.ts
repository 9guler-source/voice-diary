const MASK = "자, 천천히 편하게 말씀하세요. ";

function q(
  id: number,
  category: string,
  questionText: string,
  starter: string
): Question {
  return {
    id,
    category,
    questionText,
    ttsText: MASK + questionText,
    starter,
    isFreeTalk: false,
  };
}

